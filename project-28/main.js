// Stages Of Life:
var age = 18;
if (age < 2) {
    console.log("A Baby");
}
else if (age >= 2 && age < 4) {
    console.log("A Toddler.");
}
else if (age >= 4 && age < 13) {
    console.log("A Kiddo.");
}
else if (age >= 13 && age < 20) {
    console.log("A Teenager.");
}
else if (age >= 20 && age < 65) {
    console.log("An Adult.");
}
else {
    console.log("Eldest one.");
}
